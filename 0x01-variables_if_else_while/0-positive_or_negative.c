#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - starting point of program
 * Description: this program is used to check random number
 * for positive or negative number generated
 * Return: Always 0 (success)
 */
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;
	if (n > 0)
	{
		printf("%d is positive\n", n);
	}
	else if (n == 0)
	{
		printf("%d is zero\n", n);
	}
	else
	{
		printf("%d is negative\n", n);
	}
	return (0);
}
